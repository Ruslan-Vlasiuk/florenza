function makeRichText(text: string) {
  return {
    root: {
      children: text.split('\n\n').map((para) => ({
        type: 'paragraph',
        version: 1,
        children: [{ type: 'text', text: para, version: 1 }],
      })),
      direction: 'ltr',
      format: '',
      indent: 0,
      type: 'root',
      version: 1,
    },
  };
}
